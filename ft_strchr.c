#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;

	p = NULL;
	if (c == '\0')
	{
		return ("");
	}
	while (*s)
	{
		if (*s == c)
		{
			p = (char *)s;
			break ;
		}
		s++;
	}
	return (p);
}
